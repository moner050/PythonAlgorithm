num1 = 100
num2 = 20

# 더하기
add12 = num1 + num2
print( "add12 = ", add12 )

# 빼기
sub12 = num1 - num2
print( "sub12 = ", sub12 )

# 곱하기
mul12 = num1 * num2
print( "mul12 = ", mul12 )

# 나누기
div12 = num1 / num2
print( "div12 = ", div12 )

# 나머지 구하기
div122 = num1 % num2
print( "div122 = ", div122 )

# 나눠서 몫만 구하기
div123 = num1 // num2
print( "div123 = ", div123 )

# 제곱 구하기
square = num1 ** 2
print( "square = ", square )
